"""
evaluation/dean_analysis.py — Experiment E: Dean Quality Gate Analysis

Measures per-criterion failure rate of the Dean node across 15 tutor drafts.
10 drafts are raw LLM outputs (no Python guards — natural failure distribution).
5 drafts are crafted to test specific edge cases.

Dean criteria (from dean_check.txt):
  1. REVEAL CHECK     — concept named when reveal_permitted=False
  2. DEFINITION CHECK — "X is defined as..." pattern when not permitted
  3. GROUNDING CHECK  — factual claim not in retrieved chunks
  4. QUESTION CHECK   — response has no "?" character
  5. SYCOPHANCY CHECK — opens with "Great!" / "Excellent!" / etc.

Shows:
  - Which criteria fire most in natural LLM output
  - Dean overall pass rate
  - Per-criterion failure rate

Output: table to stdout + JSON to evaluation/results/dean_results.json

Usage:
    PYTHONPATH=. python3 evaluation/dean_analysis.py
"""

import json
import os
import sys

from anthropic import Anthropic

import config

_client = Anthropic()

CONCEPT = "ulnar nerve"
CHUNKS = [
    "The ulnar nerve is a branch of the medial cord of the brachial plexus "
    "and arises from nerve roots C8 and T1. It passes posterior to the medial "
    "epicondyle of the humerus and continues into the forearm and hand. "
    "It innervates the flexor carpi ulnaris and the medial half of the "
    "flexor digitorum profundus in the forearm.",
    "The brachial plexus is a network of nerves formed by the ventral rami of "
    "spinal nerves C5 through T1. These nerves provide motor and sensory "
    "innervation to the entire upper limb.",
]

# ── 10 natural drafts: raw teacher_socratic LLM output (no guards) ────────────
# Generated by calling teacher_socratic.txt directly, various student messages.
# reveal_permitted=False in all — we want to see what slips through naturally.

NATURAL_PROMPTS = [
    "What nerve causes the funny bone sensation?",
    "Is it the median nerve?",
    "I have no idea what nerve this is",
    "Could it be the radial nerve near the elbow?",
    "The nerve that runs through the carpal tunnel?",
    "Is it the brachial nerve?",
    "Something about the C8 nerve root maybe?",
    "I think it goes through the cubital tunnel?",
    "Is it related to the brachial plexus?",
    "I really don't know, the nerve that makes your pinky tingle?",
]

# ── 5 crafted drafts to test specific Dean criteria ───────────────────────────
# Each is designed to trigger exactly one criterion.
CRAFTED_DRAFTS = [
    {
        "label":     "C1: Sycophancy opener",
        "draft":     "Great! You're thinking along the right lines. Which nerve runs through the cubital tunnel and innervates the medial hand?",
        "expected":  ["SYCOPHANCY CHECK"],
        "reveal":    False,
    },
    {
        "label":     "C2: No question mark",
        "draft":     "Think about the nerves that run posterior to the medial epicondyle of the humerus. The structure passes between the olecranon and the medial epicondyle.",
        "expected":  ["QUESTION CHECK"],
        "reveal":    False,
    },
    {
        "label":     "C3: Hallucinated fact",
        "draft":     "Consider the nerve that originates from the C6 and C7 roots — it passes through the cubital tunnel and controls thumb opposition. Which nerve fits this description?",
        "expected":  ["GROUNDING CHECK"],
        "reveal":    False,
    },
    {
        "label":     "C4: Explicit reveal",
        "draft":     "The ulnar nerve passes posterior to the medial epicondyle. Which part of the hand does it innervate?",
        "expected":  ["REVEAL CHECK"],
        "reveal":    False,
    },
    {
        "label":     "C5: Clean draft (should pass)",
        "draft":     "Think about the nerve roots that emerge from the lower part of the brachial plexus — specifically C8 and T1. Which nerve arises from the medial cord and travels all the way down to the forearm?",
        "expected":  [],
        "reveal":    False,
    },
]


# ── Helpers ───────────────────────────────────────────────────────────────────

def _load_prompt(name: str) -> str:
    path = os.path.join(config.PROMPTS_DIR, name)
    with open(path, encoding="utf-8") as f:
        return f.read()


def _fill(template: str, **kwargs) -> str:
    result = template
    for k, v in kwargs.items():
        result = result.replace("{" + k + "}", str(v))
    return result


def _generate_draft(student_msg: str) -> str:
    """Raw teacher_socratic call — no Python leak guard, no Dean."""
    template = _load_prompt("teacher_socratic.txt")
    domain_cfg = config.DOMAIN_CONFIG.get(config.DOMAIN, {})
    prompt = _fill(
        template,
        domain_context=domain_cfg.get("system_context", config.DOMAIN),
        current_concept=CONCEPT,
        retrieved_chunks="\n\n---\n\n".join(CHUNKS),
        turn_count=0,
        reveal_permitted=False,
        max_sentences=config.MAX_RESPONSE_SENTENCES,
        question_bank="(none)",
        weak_topics="(none)",
        messages=f"Student: {student_msg}",
    )
    resp = _client.messages.create(
        model=config.PRIMARY_MODEL,
        max_tokens=300,
        messages=[{"role": "user", "content": prompt}],
    )
    return resp.content[0].text.strip()


def _run_dean(draft: str, reveal_permitted: bool) -> dict:
    """Call Dean's LLM directly and return the full JSON including failed_criteria."""
    template = _load_prompt("dean_check.txt")
    prompt = _fill(
        template,
        current_concept=CONCEPT,
        turn_count=0,
        reveal_permitted=reveal_permitted,
        retrieved_chunks="\n\n---\n\n".join(CHUNKS),
        draft_response=draft,
        max_sentences=config.MAX_RESPONSE_SENTENCES,
    )
    resp = _client.messages.create(
        model=config.PRIMARY_MODEL,
        max_tokens=300,
        messages=[{"role": "user", "content": prompt}],
    )
    raw = resp.content[0].text.strip()
    start = raw.find("{")
    end   = raw.rfind("}") + 1
    if start != -1 and end > start:
        raw = raw[start:end]
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return {"passed": True, "failed_criteria": [], "revision_instruction": "",
                "parse_error": raw[:100]}


# ── Main runner ───────────────────────────────────────────────────────────────

ALL_CRITERIA = [
    "REVEAL CHECK",
    "DEFINITION CHECK",
    "GROUNDING CHECK",
    "QUESTION CHECK",
    "SYCOPHANCY CHECK",
]


def run_experiment():
    all_results      = []
    criterion_counts = {c: 0 for c in ALL_CRITERIA}
    pass_count       = 0
    total            = 0

    print("\n" + "=" * 80)
    print("EXPERIMENT E — Dean Quality Gate Analysis")
    print("10 natural drafts (raw LLM) + 5 crafted edge cases  |  reveal=False")
    print("=" * 80)

    # ── Part 1: Natural drafts ────────────────────────────────────────────────
    print("\n--- Part 1: Natural LLM drafts (no Python guards) ---")
    print(f"  {'Student message':<45} {'Pass':<6} {'Failed criteria'}")
    print("  " + "─" * 70)

    for msg in NATURAL_PROMPTS:
        draft  = _generate_draft(msg)
        result = _run_dean(draft, reveal_permitted=False)

        passed   = result.get("passed", True)
        failed   = result.get("failed_criteria", [])
        revision = result.get("revision_instruction", "")

        if passed:
            pass_count += 1
        for c in failed:
            if c in criterion_counts:
                criterion_counts[c] += 1

        total += 1
        all_results.append({
            "type":                "natural",
            "student_message":     msg,
            "draft":               draft,
            "dean_passed":         passed,
            "failed_criteria":     failed,
            "revision_instruction": revision,
        })

        msg_short  = msg[:43] + ".." if len(msg) > 43 else msg
        pass_str   = "PASS ✓" if passed else "FAIL ✗"
        crit_str   = ", ".join(failed) if failed else "—"
        print(f"  {msg_short:<45} {pass_str:<6} {crit_str}")

    # ── Part 2: Crafted drafts ────────────────────────────────────────────────
    print("\n--- Part 2: Crafted edge-case drafts ---")
    print(f"  {'Label':<35} {'Expected fail':<20} {'Actual':<6} {'Criteria caught'}")
    print("  " + "─" * 80)

    for item in CRAFTED_DRAFTS:
        result = _run_dean(item["draft"], reveal_permitted=item["reveal"])

        passed   = result.get("passed", True)
        failed   = result.get("failed_criteria", [])

        if passed:
            pass_count += 1
        for c in failed:
            if c in criterion_counts:
                criterion_counts[c] += 1

        total += 1
        all_results.append({
            "type":            "crafted",
            "label":           item["label"],
            "draft":           item["draft"],
            "expected_fail":   item["expected"],
            "dean_passed":     passed,
            "failed_criteria": failed,
        })

        expected_str = ", ".join(item["expected"]) if item["expected"] else "none (pass)"
        actual_str   = "PASS ✓" if passed else "FAIL ✗"
        caught_str   = ", ".join(failed) if failed else "—"
        # Did Dean catch the expected criterion?
        detected     = all(e in failed for e in item["expected"])
        detect_flag  = "✓ correct" if (not item["expected"] and passed) or detected else "✗ missed"
        print(f"  {item['label']:<35} {expected_str:<20} {actual_str:<6} {caught_str}  [{detect_flag}]")

    # ── Summary ───────────────────────────────────────────────────────────────
    fail_count = total - pass_count
    print("\n" + "=" * 60)
    print("EXPERIMENT E — Summary")
    print("=" * 60)
    print(f"  Total drafts evaluated : {total}")
    print(f"  Dean PASS              : {pass_count}/{total} = {pass_count/total:.0%}")
    print(f"  Dean FAIL              : {fail_count}/{total} = {fail_count/total:.0%}")
    print(f"\n  Per-criterion failure rate (out of {total} drafts):")
    print(f"  {'Criterion':<22} {'Fires':<8} {'Rate'}")
    print("  " + "─" * 40)
    for crit in ALL_CRITERIA:
        n    = criterion_counts[crit]
        rate = n / total
        bar  = "█" * n + "░" * (total - n)
        print(f"  {crit:<22} {n}/{total:<6}  {rate:.0%}  {bar}")

    # ── Save ──────────────────────────────────────────────────────────────────
    out_path = os.path.join("evaluation", "results", "dean_results.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump({
            "total":             total,
            "pass_count":        pass_count,
            "fail_count":        fail_count,
            "pass_rate":         pass_count / total,
            "criterion_counts":  criterion_counts,
            "results":           all_results,
        }, f, indent=2)
    print(f"\nFull results → {out_path}")


if __name__ == "__main__":
    run_experiment()
